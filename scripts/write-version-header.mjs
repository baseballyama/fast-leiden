#!/usr/bin/env node
// Generates native/version_generated.h from package.json so the C++ side and
// the npm package share a single source of truth for the addon version.
//
// Run automatically before `build:native` and `install`. The output file is
// gitignored — bumping `package.json` is the only thing needed to roll a new
// version across both layers.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(here, "..");
const pkg = JSON.parse(readFileSync(join(ROOT, "package.json"), "utf8"));

if (typeof pkg.version !== "string" || pkg.version.length === 0) {
  process.stderr.write("write-version-header: package.json has no version field\n");
  process.exit(1);
}

// Reject anything that could break out of the C string literal.
if (/["\\\n\r]/.test(pkg.version)) {
  process.stderr.write(
    `write-version-header: refusing to embed version with special chars: ${JSON.stringify(pkg.version)}\n`,
  );
  process.exit(1);
}

const dest = join(ROOT, "native", "version_generated.h");
const content = `// Generated by scripts/write-version-header.mjs. Do not edit.
#pragma once
#define FAST_LEIDEN_VERSION "${pkg.version}"
`;

// Only rewrite on change so node-gyp's dependency tracking doesn't see a churn.
const prev = existsSync(dest) ? readFileSync(dest, "utf8") : "";
if (prev !== content) {
  writeFileSync(dest, content);
}
